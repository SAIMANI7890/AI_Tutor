"""
Examination API Routes - Phase 4C
======================================================
Exposes the full examination lifecycle via REST endpoints.

Base prefix:  /api/v1/exams   (registered in router.py)

Endpoints
---------
POST   /generate              → Generate a new exam
GET    /                      → List user's exams
GET    /history               → Exam history (alias for list with semantic name)
GET    /{test_id}             → Full exam detail (metadata + questions)
GET    /{test_id}/questions   → Questions only (student-safe)
POST   /{test_id}/answer      → Save / autosave one answer
GET    /{test_id}/answers     → Retrieve saved answers (page-refresh recovery)
POST   /{test_id}/submit      → Submit the exam

Security
--------
All endpoints require a valid JWT Bearer token.
Only the exam owner can view, answer, or submit their exam.
"""
