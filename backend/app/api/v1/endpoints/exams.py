@router.post(
    "/generate",
    response_model=APISuccess,
    status_code=status.HTTP_201_CREATED,
    summary="Generate Exam",
    description=(
        "Generate a new AI-powered examination. "
        "Questions are retrieved from the RAG vector store and generated by Gemini. "
        "Supports MCQ, Fill-in-the-Blanks, Short Answer, and Long Answer types.\n\n"
        "**Rate:** This endpoint is compute-intensive — avoid calling it in rapid succession."
    ),
    responses={
        201: {
            "description": "Exam generated successfully",
            "content": {
                "application/json": {
                    "example": {
                        "success": True,
                        "message": "Exam generated successfully",
                        "data": {
                            "test_id": "123e4567-e89b-12d3-a456-426614174000",
                            "question_count": 10,
                            "status": "GENERATED",
                        },
                    }
                }
            },
        },
        422: {"description": "Validation error (invalid categories / type / count)"},
        429: {"description": "Rate limit exceeded (max 5 exams per hour)"},
        500: {"description": "Generation failed (Gemini / database error)"},
    },
)
def generate_exam(
    request_body: ExamGenerateRequest,
    current_user: User = Depends(get_current_user),
    db: Session = Depends(get_db),
):
    """
    **Generate a new examination.**

    - Validates categories, question type, and count.
    - Retrieves relevant content from ChromaDB (RAG).
    - Calls Gemini AI to generate questions.
    - Persists the exam and questions to PostgreSQL.
    - Returns the exam ID and metadata.

    **Request Body:**
    - `categories`: e.g. `["History", "Politics"]`
    - `question_type`: one of `MCQ | FILL_BLANKS | SHORT_ANSWER | LONG_ANSWER`
    - `question_count`: integer 1–10

    **Security:** Requires Bearer token.
    **Rate Limit:** 5 exams per hour per user.
    """
    # Check rate limit BEFORE processing
    check_exam_generation_limit(current_user.id)
    
    logger.info(
        "POST /exams/generate | user=%s type=%s count=%s",
        current_user.id, request_body.question_type, request_body.question_count,
    )

    data = ExamService.generate_exam(
        db=db,
        user_id=current_user.id,
        categories=request_body.categories,
        question_type=request_body.question_type,
        question_count=request_body.question_count,
    )

    return APISuccess(
        success=True,
        message="Exam generated successfully",
        data=data,
    )


