# ENDPOINT 9 — Submitted & Evaluated tests (MUST be before /{test_id})
# ===========================================================================

@router.get(
    "/submitted",
    response_model=APISuccess,
    summary="Get Submitted Tests",
    description=(
        "Return all tests with status SUBMITTED or EVALUATED for the authenticated user. "
        "Used by the Evaluation module to show tests that are ready for AI evaluation."
    ),
    responses={
        200: {"description": "Submitted tests returned"},
    },
)
def get_submitted_tests(
    current_user: User = Depends(get_current_user),
    db: Session = Depends(get_db),
):
    """
    **Get all submitted and evaluated tests.**

    Returns tests with status SUBMITTED or EVALUATED, ordered newest first.
    These are tests where the student has completed answering and can now
    trigger AI evaluation.

    **Security:** Requires Bearer token.
    """
    from app.repositories.test_repository import TestRepository
    from app.models.enums import TestStatus

    submitted = TestRepository.get_by_user_and_status(
        db, user_id=current_user.id, status=TestStatus.SUBMITTED
    )
    evaluated = TestRepository.get_by_user_and_status(
        db, user_id=current_user.id, status=TestStatus.EVALUATED
    )

    all_tests = submitted + evaluated
    # Sort by created_at descending
    all_tests.sort(key=lambda t: t.created_at, reverse=True)

    tests_data = [
        {
            "id": str(t.id),
            "subject": t.subject,
            "question_type": t.question_type.value if hasattr(t.question_type, "value") else t.question_type,
            "selected_categories": t.selected_categories,
            "question_count": t.question_count,
            "status": t.status.value if hasattr(t.status, "value") else t.status,
            "created_at": t.created_at.isoformat(),
            "started_at": t.started_at.isoformat() if t.started_at else None,
            "completed_at": t.completed_at.isoformat() if t.completed_at else None,
        }
        for t in all_tests
    ]

    return APISuccess(
        success=True,
        message=f"Retrieved {len(tests_data)} submitted tests",
        data={"tests": tests_data, "count": len(tests_data)},
    )


